var numero_1 = parseInt(prompt("Por favor, ingresa el primero de los números:"));
var numero_2 = parseInt(prompt("Por favor, ingresa el segundo de los números:"));


if(numero_1 > numero_2){
    alert("El número más grande que ingresaste es" + numero_1);
}
else{
    alert ("El número más grande que ingresaste es" + numero_2);
}